package nemosofts.driving.exam.item;


public class ItemLanguage {

    private final String id;
    private final String name;

    public ItemLanguage(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

}
